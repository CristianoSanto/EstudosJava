package com.extreme.cursojava.aula11;

public class LiteraisHexadecimaisOctaisBinarios {

	public static void main(String[] args) {

		int decVal = 26;
		int hexVal = 0x1a;
		int octVal = 032;
		int binVal = 0b11010;
		
		System.out.println("Valor em decimal: " + decVal);
		System.out.println("Valor em hexadecimal: " + hexVal);
		System.out.println("Valor em octal: " + octVal);
		System.out.println("Valor em binario: " + binVal);
	}

}
