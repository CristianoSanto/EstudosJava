package com.extreme.cursojava.aula15.Labs;

import java.util.Scanner;

public class Exercicio05 {

	public static void main(String[] args) {

		Scanner scan = new Scanner(System.in);
		
		System.out.println("Digite a primeira nota: ");
		double nota1 = scan.nextDouble();
		
		System.out.println("Digite a segunda nota: ");
		double nota2 = scan.nextDouble();
		
		double media = (nota1 + nota2) / 2;
		
		if  (media == 10) {
			System.out.println("Aprovado com distinção! você tiro nota " + media + " PARABÈNS"); 
		
		}
		
		else if (media >= 7) {
			System.out.println("Aprovado! sua nota foi " + media);  
		}
		else {
			System.out.println("Reprovado! sua nota foi " + media); 
		}
	}

}
